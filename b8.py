import re  # Импортируем модуль для работы с регулярными выражениями

# Создаём шаблон (регулярное выражение), чтобы найти номер в формате: 8 xxx-xxx
pattern = re.compile(r'8\s*(\d{3})-(\d{3})')

found = False  # Эта переменная показывает, нашли ли мы номер или нет

# Начинаем читать строки одну за другой
while True:
    try:
        line = input()  # Читаем строку от пользователя (с клавиатуры)
        if line == "":
            break  # Если строка пустая — заканчиваем ввод

        # Заменяем найденный номер на нужный формат: +7 3467 8 xxx-xxx
        new_line = pattern.sub(r'+7 3467 8 \1-\2', line)

        # Если строка изменилась, значит номер найден
        if new_line != line:
            found = True

        # Печатаем строку (уже с заменой, если была)
        print(new_line)
    except EOFError:
        break  # Если ввод завершён (например, нажали Ctrl+D), выходим

# Если номера не были найдены ни в одной строке — выводим сообщение
if not found:
    print("Not found")
